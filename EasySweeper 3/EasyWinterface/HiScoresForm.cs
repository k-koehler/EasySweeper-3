        private void ProcessClick(object sender, EventArgs args) {
            var button = (Button)sender;
            switch (button.Text) {
                case "5 Man":
                FindHiscores(Tasks.CATEGORY._5s);
                break;
                case "4S1L":
                FindHiscores(Tasks.CATEGORY._4s);
                break;
                case "Trio":
                FindHiscores(Tasks.CATEGORY._3s);
                break;
                case "Duo":
                FindHiscores(Tasks.CATEGORY.Duo);
                break;
                case "Solo":
                FindHiscores(Tasks.CATEGORY.Solo);
                break;
                case "Medium":
                FindHiscores(Tasks.CATEGORY.Med);
                break;
                case "Small":
                FindHiscores(Tasks.CATEGORY.Small);
                break;
                case "C1":
                FindHiscores(Tasks.CATEGORY.C1);
                break;
                default:
                MessageBox.Show("This should never happen.");
                break;

            }
        }

